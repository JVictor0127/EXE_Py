#tipos primitivos e saidas de dados/ exercicio 003
n1 = int(input('Digitre um numero: '))
n2 = int(input('Digite outro numero: '))
s = n1+n2
m = n1*n2
d = n1/n2
sb = n1-n2
print('A soma entre {} e {} é {}'.format(n1, n2, s))
print('A mutiplicação entre {} e {} é {}'.format(n1, n2, m))
print('A divisão entre {} e {} é {}'.format(n1, n2, d))
print('A subtração entre {} e {} é {}'.format(n1, n2, sb))
